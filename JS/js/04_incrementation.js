// L'incrémentation est l'operation qui consiste à ajouter
// 1 (par extension toute valeur entière fixée)
// à un competeur. L'opération inverse est la décrementation 
// Elle consiste à retirer 1 au compteur.

// L'operateur d'incrément ajoute une unité a son opérand
// et renvoie une valeur.

// -----------INCREMENTATION ---------------------

var nb1 = 1;
nb1 = nb1+1;
// j'augmente nb1 de 1
console.log(nb1);

// -------------------ECRITURE SIMPLIFIEE-------------
nb1++;
console.log(nb1);
// affichage=>nb1=3

++nb1;
console.log(nb1);
// affichage => nb1 = 4;

// ------------------DECREMENTATION-------------


nb1=nb1-1;
console.log(nb1);

// ECRITURE SIMPLIFIE----------
nb1--;
console.log(nb1);
// affichage nb1 => 2